    public static class Peticion extends AsyncTask<Void, Void, Void> {

        @Override
        protected Void doInBackground(Void... voids) {
            final String url = "http://192.168.1.73:3000/";
            Retrofit retrofit = new Retrofit.Builder()
                    .baseUrl(url)
                    .addConverterFactory(GsonConverterFactory.create())
                    .build();
            ServiceUsuario serviciosUsuario = retrofit.create(ServiceUsuario.class);
            Call<List<ResponseUsuario>> response = serviciosUsuario.getUsers();
            try {
                for (ResponseUsuario usuario : response.execute().body()) {
                    Log.e("**************", usuario.getCorreo());
                }
            } catch(IOException e){
                Log.e("ERROR", e.toString());
            }
            return null;
        }
    }

    @Override
    public void onBackPressed() {
        DrawerLayout drawer = (DrawerLayout) findViewById(R.id.drawer_layout);
        if (drawer.isDrawerOpen(GravityCompat.START)) {
            drawer.closeDrawer(GravityCompat.START);
        } else {
            super.onBackPressed();
        }
    }

